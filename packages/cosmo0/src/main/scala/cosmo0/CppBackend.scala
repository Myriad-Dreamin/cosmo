        )

      def typeName(id: LirDeclId): String =
        typeNames.getOrElse(id, safeIdent(id.value, "T"))

      def aliasName(id: LirDeclId): String =
        aliasNames.getOrElse(id, safeIdent(id.value, "A"))

      def globalName(id: LirDeclId): String =
        globalNames.getOrElse(id, safeIdent(id.value, "g"))

      def functionName(id: LirDeclId): String =
        functionNames.getOrElse(id, safeIdent(id.value, "fn"))

