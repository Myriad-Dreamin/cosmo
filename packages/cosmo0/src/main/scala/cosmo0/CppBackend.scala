      def apply(env: DeclEnv): BackendNames =
        new BackendNames(env)

    private def uniqueNames[K](
        values: List[(K, String)],
        prefix: String,
        keyText: K => String,
    ): Map[K, String] =
      val used = mutable.LinkedHashMap.empty[String, Int]
      values.foldLeft(Map.empty[K, String]) { case (acc, (key, raw)) =>
        val base = safeIdent(raw, prefix)
        val count = used.getOrElse(base, 0)
        used.update(base, count + 1)
        val value =
          if count == 0 then base
          else s"${base}_${stableHash(keyText(key))}"
        acc.updated(key, value)
      }

    private def stableHash(value: String): String =
      val raw = value.foldLeft(0x811c9dc5) { (hash, char) =>
        (hash ^ char.toInt) * 0x01000193
      }
      java.lang.Integer.toUnsignedString(raw, 36)

    private def fieldName(value: String): String =
      safeIdent(value, "field")

    private def payloadName(payload: LirVariantPayload, index: Int): String =
      payload.name.fold(s"field$index")(fieldName)

    private def variantStructName(value: String): String =
      s"Variant_${safeIdent(value, "case")}"

    private def safeIdent(value: String, fallback: String): String =
      val builder = new StringBuilder
      value.foreach { char =>
        if char.isLetterOrDigit || char == '_' then builder.append(char)
        else if builder.nonEmpty && builder.last != '_' then builder.append('_')
      }
      val raw = builder.toString.stripPrefix("_").stripSuffix("_")
      val base = if raw.nonEmpty then raw else fallback
      val prefixed =
        if base.headOption.exists(_.isDigit) then s"${fallback}_$base"
        else base
      if cppKeywords.contains(prefixed) then s"${prefixed}_"
      else prefixed

    private def quote(value: String): String =
      val builder = new StringBuilder
      builder.append('"')
      value.foreach {
        case '\\' => builder.append("\\\\")
        case '"'  => builder.append("\\\"")
        case '\n' => builder.append("\\n")
        case '\r' => builder.append("\\r")
        case '\t' => builder.append("\\t")
        case char if char.isControl =>
          builder.append("\\x")
          builder.append(f"${char.toInt}%02x")
        case char =>
          builder.append(char)
      }
      builder.append('"')
      builder.toString

